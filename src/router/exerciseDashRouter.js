// exerciseDashRouter.js

import express from "express";
import * as service from "../service/exerciseDashService.js";
export const router = express.Router();

// 1-1. dash (scatter - today) -------------------------------------------------------------------->
router.get("/scatter/today", async (req, res) => {
  try {
    let result = await service.scatterToday (
      req.query.customer_id
    );
    if (result) {
      res.json({
        status: "success",
        msg: "조회 성공",
        result: result
      });
    }
    else {
      res.json({
        status: "fail",
        msg: "조회 실패",
        result: null
      });
    }
  }
  catch (err) {
    console.error(err);
    res.status(500).json({
      status: "error",
      error: err.toString()
    });
  }
});

